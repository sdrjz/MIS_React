import React from 'react';
import ReactDOM from 'react-dom/client';
import { BrowserRouter } from 'react-router-dom';
import './index.css';
import App from './App';
import reportWebVitals from './reportWebVitals';

const root = ReactDOM.createRoot(document.getElementById('root'));

// read <base> if present; default to /MIS
const baseHref = document.querySelector('base')?.getAttribute('href') || '/MIS/';
const basename = baseHref.replace(/\/+$/, ''); // strip trailing "/"

root.render(
  <React.StrictMode>
    <BrowserRouter basename={basename}>
      <App />
    </BrowserRouter>
  </React.StrictMode>
);

reportWebVitals();
